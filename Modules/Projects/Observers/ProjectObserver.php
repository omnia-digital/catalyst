<?php

namespace Modules\Projects\Observers;

use Modules\Projects\Models\Project;

class ProjectObserver
{
    /**
     * Handle the Project "created" event.
     *
     * @param  \App\Models\Project  $project
     * @return void
     */
    public function created(Project $project)
    {
        activity()->by($project->owner)->on($project)->log("Project $project->title created");
    }

    /**
     * Handle the Project "updated" event.
     *
     * @param  \App\Models\Project  $project
     * @return void
     */
    public function updated(Project $project)
    {
        activity()->by($project->owner)->on($project)->log("Project $project->title updated");
    }

    /**
     * Handle the Project "deleted" event.
     *
     * @param  \App\Models\Project  $project
     * @return void
     */
    public function deleted(Project $project)
    {
        activity()->by($project->owner)->on($project)->log("Project $project->title deleted");
    }

    /**
     * Handle the Project "restored" event.
     *
     * @param  \App\Models\Project  $project
     * @return void
     */
    public function restored(Project $project)
    {
        activity()->by($project->owner)->on($project)->log("Project $project->title restored");
    }

    /**
     * Handle the Project "force deleted" event.
     *
     * @param  \App\Models\Project  $project
     * @return void
     */
    public function forceDeleted(Project $project)
    {
        activity()->by($project->owner)->on($project)->log("Project $project->title force deleted");
    }
}
